package m11_operators_part1;

public class SwapValue {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 7 ;


        int temp;

        temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println( "num1 = " + num1 + " , " + "num2 = " + num2);


    }
}
